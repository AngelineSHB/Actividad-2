    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigo.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        Date fechaNacimiento = jDateChooser1.getDate();
        
        // Validar que todos los campos estén completos
        if (codigo.isEmpty() || nombre.isEmpty() || apellido.isEmpty() || fechaNacimiento == null) {
        JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
        return;
        }
        
        // Crear el objeto Alumno
       Alumno alumno = new Alumno(codigo, nombre, apellido, fechaNacimiento);
       
        // Agregar los datos del alumno a la tabla
    modeloTabla.addRow(new Object[]{alumno.getCodigo(), alumno.getNombre(), alumno.getApellido(), 
                                    new SimpleDateFormat("dd/MM/yyyy").format(alumno.getFechaNacimiento()), 
                                    alumno.getEdad()});

    // Limpiar los campos después de agregar al alumno
    txtCodigo.setText("");
    txtNombre.setText("");
    txtApellido.setText("");
    jDateChooser1.setDate(null);
    txtEdad.setText("");
}


    private int calcularEdad(Date fechaNacimiento) {
        if (fechaNacimiento == null) {
            return -1; // Maneja el caso donde la fecha de nacimiento es nula
        }

        java.util.Calendar fechaNac = java.util.Calendar.getInstance();
        fechaNac.setTime(fechaNacimiento);

        java.util.Calendar fechaAct = java.util.Calendar.getInstance(); // Obtén la fecha actual

        int edad = fechaAct.get(java.util.Calendar.YEAR) - fechaNac.get(java.util.Calendar.YEAR);

        // Ajustar si aún no ha cumplido años este año
        if (fechaAct.get(java.util.Calendar.DAY_OF_YEAR) < fechaNac.get(java.util.Calendar.DAY_OF_YEAR)) {
            edad--;
        }

        return edad;
    }

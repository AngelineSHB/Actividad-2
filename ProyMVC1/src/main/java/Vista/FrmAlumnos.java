    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
    int selectedRow = jTableUsuarios.getSelectedRow();
        if (selectedRow != -1)
    {
            // Obtener datos de la fila seleccionada
            String codigo = (String) modeloTabla.getValueAt(selectedRow, 0);
            String nombre = (String) modeloTabla.getValueAt(selectedRow, 1);
            String apellido = (String) modeloTabla.getValueAt(selectedRow, 2);
            String fechaStr = (String) modeloTabla.getValueAt(selectedRow, 3);

            try 
            {
                Date fechaNacimiento = new SimpleDateFormat("dd/MM/yyyy").parse(fechaStr);

                // Cargar datos en los campos de texto
                txtCodigo.setText(codigo);
                txtNombre.setText(nombre);
                txtApellido.setText(apellido);
                jDateChooser1.setDate(fechaNacimiento);
                txtEdad.setText(String.valueOf(calcularEdad(fechaNacimiento)));
            } 
            catch (Exception e) 
            {
                e.printStackTrace();
            }
        } 
        else {
            JOptionPane.showMessageDialog(this, "Selecciona un alumno para modificar.");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTableUsuarios.getSelectedRow();

        if (selectedRow != -1) 
{
            // Eliminar la fila seleccionada del modelo de la tabla
            modeloTabla.removeRow(selectedRow);

            // Limpiar los campos después de eliminar
            txtCodigo.setText("");
            txtNombre.setText("");
            txtApellido.setText("");
            jDateChooser1.setDate(null);
            txtEdad.setText("");
        } 
else {
            JOptionPane.showMessageDialog(this, "Selecciona un usuario para eliminar.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

